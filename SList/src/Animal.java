
public class Animal {
	private String name;	
	private String city;	
	private String phone;	
	
Animal(String name,String city,String phone){
	this.name=name;
	this.city=city;
	this.phone=phone;
	
}
	
String getName() {
	return name;
}
String getCity() {
	return city;
}
String getPhone() {
return phone;	
}
public String toString() {
	return name;
}
}
